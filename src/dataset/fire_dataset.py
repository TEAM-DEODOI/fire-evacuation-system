"""
PyTorch Dataset for fire simulation scenarios.

Loads ``(input_t, target_{t+1})`` pairs from the processed HDF5 file
produced by ``src.data_pipeline.build_dataset`` (or the synthetic
substitute generated by ``scripts/build_synthetic_dataset.py``).

Each scenario contributes ``N_TIMESTEPS - 1 = 30`` pairs:
``input[t]`` (5-channel) → ``target[t + 1]`` (3-channel) for ``t ∈ [0, 30)``.

Caching strategy: the entire split is loaded into memory in ``__init__``
(~107 MB total for 30 scenarios). This avoids per-call HDF5 file handle
issues with multiprocessing DataLoader workers.
"""
from __future__ import annotations

from pathlib import Path
from typing import List, Literal, Tuple

import h5py
import numpy as np
import torch
from torch.utils.data import Dataset

from src.shared.constants import GRID_SHAPE, N_INPUT_CHANNELS, N_OUTPUT_CHANNELS, N_TIMESTEPS

_PAIRS_PER_SCENARIO: int = N_TIMESTEPS - 1  # 30

_SPLIT_KEY = {
    "train": "train_indices",
    "val": "val_indices",
    "ood": "ood_indices",
}


class FireDataset(Dataset):
    """PyTorch Dataset over ``(input_t, target_{t+1})`` pairs.

    Args:
        dataset_path: Path to the HDF5 file (``data/processed/dataset.h5``
            for real data, or a synthetic file from
            ``scripts/build_synthetic_dataset.py``).
        split: One of ``"train"``, ``"val"``, ``"ood"``.
        augment: Reserved for future random-flip augmentation. Currently a
            no-op; reading it keeps the constructor compatible with the
            skeleton signature.

    Raises:
        FileNotFoundError: If ``dataset_path`` does not exist.
        ValueError: If ``split`` is unknown or an in-file tensor has the
            wrong shape.
    """

    def __init__(
        self,
        dataset_path: Path,
        split: Literal["train", "val", "ood"] = "train",
        augment: bool = False,
    ) -> None:
        dataset_path = Path(dataset_path)
        if not dataset_path.exists():
            raise FileNotFoundError(f"Dataset not found: {dataset_path}")
        if split not in _SPLIT_KEY:
            raise ValueError(
                f"split must be one of {list(_SPLIT_KEY)}, got {split!r}"
            )

        self.dataset_path: Path = dataset_path
        self.split: str = split
        self.augment: bool = augment

        self._inputs: List[np.ndarray] = []
        self._targets: List[np.ndarray] = []
        self._scenario_ids: List[int] = []

        with h5py.File(dataset_path, "r") as f:
            indices = np.asarray(f["metadata"][_SPLIT_KEY[split]][...]).tolist()
            for sid in indices:
                key = f"scenario_{int(sid):03d}"
                if key not in f:
                    raise ValueError(f"missing group {key!r} in {dataset_path}")
                grp = f[key]
                inp = np.asarray(grp["input"][...], dtype=np.float32)
                tgt = np.asarray(grp["target"][...], dtype=np.float32)
                self._validate_scenario_shapes(inp, tgt, key)
                self._inputs.append(inp)
                self._targets.append(tgt)
                self._scenario_ids.append(int(sid))

        # Empty split is allowed (D-024: val/ood may be 0 until separate sims
        # are added). Downstream Trainer detects via ``len(dataset) == 0`` and
        # skips val pass.

    @staticmethod
    def _validate_scenario_shapes(
        inp: np.ndarray, tgt: np.ndarray, key: str
    ) -> None:
        nx, ny, nz = GRID_SHAPE
        expected_in = (N_TIMESTEPS, N_INPUT_CHANNELS, nx, ny, nz)
        expected_tg = (N_TIMESTEPS, N_OUTPUT_CHANNELS, nx, ny, nz)
        if inp.shape != expected_in:
            raise ValueError(
                f"{key}/input shape {inp.shape} != expected {expected_in}"
            )
        if tgt.shape != expected_tg:
            raise ValueError(
                f"{key}/target shape {tgt.shape} != expected {expected_tg}"
            )

    def __len__(self) -> int:
        return len(self._inputs) * _PAIRS_PER_SCENARIO

    def __getitem__(self, idx: int) -> Tuple[torch.Tensor, torch.Tensor]:
        if idx < 0 or idx >= len(self):
            raise IndexError(f"index {idx} out of range (len={len(self)})")

        scenario_pos, frame_idx = divmod(idx, _PAIRS_PER_SCENARIO)
        x = self._inputs[scenario_pos][frame_idx]            # (5, 60, 40, 6)
        y = self._targets[scenario_pos][frame_idx + 1]       # (3, 60, 40, 6)
        return torch.from_numpy(x.copy()), torch.from_numpy(y.copy())

    @property
    def scenario_ids(self) -> List[int]:
        """Return the scenario IDs comprising this split (for debugging/logging)."""
        return list(self._scenario_ids)


# ─────────────────────────────────────────────────────────────────────────────
# Self-test (run with ``python -m src.dataset.fire_dataset``).
# ─────────────────────────────────────────────────────────────────────────────
if __name__ == "__main__":
    import tempfile

    print("=" * 60)
    print("FireDataset self-test")
    print("=" * 60)

    errors: list[str] = []

    # Build a tiny synthetic HDF5 inline so the self-test is self-contained.
    nx, ny, nz = GRID_SHAPE
    with tempfile.TemporaryDirectory() as tmp:
        h5_path = Path(tmp) / "synthetic.h5"
        rng = np.random.default_rng(0)

        n_scenarios = 6  # 4 train / 1 val / 1 ood
        with h5py.File(h5_path, "w") as f:
            for sid in range(n_scenarios):
                grp = f.create_group(f"scenario_{sid:03d}")
                grp.create_dataset(
                    "input",
                    data=rng.uniform(0, 1, size=(N_TIMESTEPS, N_INPUT_CHANNELS, nx, ny, nz)).astype(np.float32),
                )
                grp.create_dataset(
                    "target",
                    data=rng.uniform(0, 1, size=(N_TIMESTEPS, N_OUTPUT_CHANNELS, nx, ny, nz)).astype(np.float32),
                )
            f.create_dataset("mask", data=np.ones(GRID_SHAPE, dtype=np.float32))
            meta = f.create_group("metadata")
            meta.create_dataset("train_indices", data=np.array([0, 1, 2, 3], dtype=np.int32))
            meta.create_dataset("val_indices", data=np.array([4], dtype=np.int32))
            meta.create_dataset("ood_indices", data=np.array([5], dtype=np.int32))

        # ── Test 1: split sizes ─────────────────────────────────────────────
        print("\n[Test 1] Split lengths")
        train = FireDataset(h5_path, split="train")
        val = FireDataset(h5_path, split="val")
        ood = FireDataset(h5_path, split="ood")
        print(f"  train = {len(train)}  (expect 4 × 30 = 120)")
        print(f"  val   = {len(val)}    (expect 1 × 30 = 30)")
        print(f"  ood   = {len(ood)}    (expect 1 × 30 = 30)")
        if len(train) != 120 or len(val) != 30 or len(ood) != 30:
            errors.append("split sizes wrong")

        # ── Test 2: __getitem__ shape and dtype ─────────────────────────────
        print("\n[Test 2] __getitem__ returns correct shapes")
        x, y = train[0]
        print(f"  x.shape={tuple(x.shape)} y.shape={tuple(y.shape)}")
        if x.shape != (N_INPUT_CHANNELS, nx, ny, nz):
            errors.append(f"x shape {tuple(x.shape)}")
        if y.shape != (N_OUTPUT_CHANNELS, nx, ny, nz):
            errors.append(f"y shape {tuple(y.shape)}")
        if x.dtype != torch.float32 or y.dtype != torch.float32:
            errors.append("dtype not float32")

        # ── Test 3: pair indexing — first scenario, last pair ───────────────
        print("\n[Test 3] Pair indexing maps to (input[t], target[t+1])")
        # Grab via dataset API…
        x_last, y_last = train[_PAIRS_PER_SCENARIO - 1]  # last pair of scenario 0
        # …and directly from HDF5 to compare.
        with h5py.File(h5_path, "r") as f:
            inp_direct = f["scenario_000"]["input"][_PAIRS_PER_SCENARIO - 1]   # frame 29
            tgt_direct = f["scenario_000"]["target"][_PAIRS_PER_SCENARIO]      # frame 30
        if not np.allclose(x_last.numpy(), inp_direct):
            errors.append("x last-pair mismatch")
        if not np.allclose(y_last.numpy(), tgt_direct):
            errors.append("y last-pair mismatch")
        print("  PASS" if not errors else "  FAIL")

        # ── Test 4: out-of-range index raises ───────────────────────────────
        print("\n[Test 4] Out-of-range index raises IndexError")
        try:
            _ = train[len(train)]
        except IndexError:
            print("  PASS")
        else:
            errors.append("OOB index did not raise")

        # ── Test 5: bad split name raises ───────────────────────────────────
        print("\n[Test 5] Bad split name raises ValueError")
        try:
            FireDataset(h5_path, split="bogus")  # type: ignore[arg-type]
        except ValueError:
            print("  PASS")
        else:
            errors.append("bad split did not raise")

        # ── Test 6: missing file raises ─────────────────────────────────────
        print("\n[Test 6] Missing file raises FileNotFoundError")
        try:
            FireDataset(Path(tmp) / "nope.h5", split="train")
        except FileNotFoundError:
            print("  PASS")
        else:
            errors.append("missing file did not raise")

    if errors:
        print("\nFAIL")
        for e in errors:
            print(f"  - {e}")
        raise SystemExit(1)

    print("\n" + "=" * 60)
    print("PASS")
    print("=" * 60)
